    "ă" => "a", "ắ" => "a", "ặ" => "a", "ằ" => "a", "ẳ" => "a", "ẵ" => "a",
    "â" => "a", "ấ" => "a", "ầ" => "a", "ẩ" => "a", "ẫ" => "a", "ậ" => "a",
    "À" => "a", "Á" => "a", "Ả" => "a", "Ã" => "a", "Ạ" => "a",
    "Ă" => "a", "Ắ" => "a", "Ặ" => "a", "Ằ" => "a", "Ẳ" => "a", "Ẵ" => "a",
    "Â" => "a", "Ấ" => "a", "Ầ" => "a", "Ẩ" => "a", "Ẫ" => "a", "Ậ" => "a",
    "đ" => "d", "Đ" => "d",
    "è" => "e", "é" => "e", "ẻ" => "e", "ẽ" => "e", "ẹ" => "e",
    "ê" => "e", "ế" => "e", "ề" => "e", "ể" => "e", "ễ" => "e", "ệ" => "e",
