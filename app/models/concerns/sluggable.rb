    "Ư" => "u", "Ứ" => "u", "Ừ" => "u", "Ử" => "u", "Ữ" => "u", "Ự" => "u",
    "ỳ" => "y", "ý" => "y", "ỷ" => "y", "ỹ" => "y", "ỵ" => "y",
    "Ỳ" => "y", "Ý" => "y", "Ỷ" => "y", "Ỹ" => "y", "Ỵ" => "y"
  }.freeze

  VIET_PATTERN = Regexp.union(VIET_MAP.keys).freeze

  module ClassMethods
    def slugify(text, max_length: 50)
      return "" if text.blank?
      text.to_s
          .encode("UTF-8", invalid: :replace, undef: :replace, replace: "")
          .unicode_normalize(:nfc)   # ensure precomposed form so map matches
          .gsub(VIET_PATTERN, VIET_MAP)
          .downcase
          .gsub(/[^a-z0-9]+/, "-")
          .gsub(/\A-+|-+\z/, "")
          .first(max_length)
    end
  end
end
